package HelloWorldAndPrimitive;

public class Main {
	public static void main(String[] args)
	{
	System.out.printf("\n Hello World!!");
	
	// Integer Value
	int integer = 5;
	System.out.print("\n Our integer value is ::" + integer);
	
	// boolean value
	
	boolean  a =false;
	System.out.print("\n Our Boolean value is:::"+a);
	
	//sort value
	
	short temperature =-200;
	System.out.println("\n Our Short value is:::"+temperature);
	
	//byte value 
	 
	byte range = 124;
	System.out.println(" Our byte value is::"+range);
	
	//double
	 
	double b = 34.4;
	System.out.println(" Our double value is:::"+b);
	
	//long int value
	
	long  c = -233214324321414l;
	System.out.println(" Our long int value is::::"+c);
	
	//float value
	float e = 23.443f;
	System.out.println(" Our Float value is :::"+e);
	
	}

}
